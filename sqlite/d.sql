DROP DATABASE IF EXISTS cmdev;

CREATE DATABASE cmdev CHARACTER SET big5;

USE cmdev;

DROP TABLE IF EXISTS emp;
CREATE TABLE emp (   #設定欄位名稱&資料類型
  empno INT NOT NULL,
  ename VARCHAR(16) NOT NULL,
  job VARCHAR(16),
  manager INT,
  hiredate DATE,
  salary float(7, 2),
  comm float(7,2),
  deptno INT,
  PRIMARY KEY (empno) 
#PRIMARY KEY約束唯一的標示表中的每個記錄，必須包含UNIQUE值，並且不能包含NULL值，一個表只能有一個主鍵。並且在表中，此主鍵可以包含單列或多列（字段）。
);
